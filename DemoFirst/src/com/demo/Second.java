package com.demo;
import java.util.*;

public class Second {
	public static void main(String[] args) {
		
		int value = 9/4;
		System.out.println(value);
		
		float value2 = 100f/3f;
		System.out.println(value2);
		
		double value3 = 100d/3d;
		System.out.println(value3);
		
		int r = 10;
		double area = 3.14 * r *r;
		System.out.println("Area of circle :"+area);
		
		char ch = 65;
		System.out.println(ch); // returns the ascii value of the number
		
		short shortMin = 32767;
		short shortMax = -32768;
		
		System.out.println("Minimum range of short :"+shortMin);
		System.out.println("Maximum range of short :"+shortMax);
		
		
		
		
		
		
	}

}
